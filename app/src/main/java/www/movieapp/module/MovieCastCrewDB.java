public class MovieCastCrewDB {

    String castFilePath;
    String CrewFilePath;
    String castFullName;
    String crewFullName;
    String castCharater;
    String crewjob;

    public String getCastFilePath() {
        return castFilePath;
    }

    public void setCastFilePath(String castFilePath) {
        this.castFilePath = castFilePath;
    }

    public String getCrewFilePath() {
        return CrewFilePath;
    }

    public void setCrewFilePath(String crewFilePath) {
        CrewFilePath = crewFilePath;
    }

    public String getCastFullName() {
        return castFullName;
    }

    public void setCastFullName(String castFullName) {
        this.castFullName = castFullName;
    }

    public String getCrewFullName() {
        return crewFullName;
    }

    public void setCrewFullName(String crewFullName) {
        this.crewFullName = crewFullName;
    }

    public String getCastCharater() {
        return castCharater;
    }

    public void setCastCharater(String castCharater) {
        this.castCharater = castCharater;
    }

    public String getCrewjob() {
        return crewjob;
    }

    public void setCrewjob(String crewjob) {
        this.crewjob = crewjob;
    }


}
